package com.dianrong.common.uniauth.client;

public class DomainDefine {
	private String domainName;
	private String domainDefaultUrl;

	public String getDomainName() {
		return domainName;
	}

	public void setDomainName(String domainName) {
		this.domainName = domainName;
	}

	public String getDomainDefaultUrl() {
		return domainDefaultUrl;
	}

	public void setDomainDefaultUrl(String domainDefaultUrl) {
		this.domainDefaultUrl = domainDefaultUrl;
	}
	
	
}
